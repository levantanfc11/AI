a = int(input("nhập số nguyên dương a = "))
b = int(input("nhập số nguyên dương b = "))
#tong2so
c = a + b 
print("Tổng của hai số là =", c)
#tich2so
c = a * b
print("Tích của hai số là =", c)
#hieu2so
c = a - b
print("Hiệu của 2 số là =", c)
#chialayphandu
c = a % b
print("Phép chia lấy dư của 2 số là =", c)
#chialayphannguyen
c = a // b 
print("Phép chia lấy phần nguyên của 2 số là =", c)
